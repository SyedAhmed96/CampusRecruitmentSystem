package com.example.ahmed.campusrecruitmentsystem;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import java.util.ArrayList;

/**
 * Created by Ishaq Hassan on 8/13/2017.
 */

public class StudentPagerAdapter extends FragmentStatePagerAdapter {
    ArrayList<StudentBaseFragment> fragments;
    public StudentPagerAdapter(FragmentManager fm, ArrayList<StudentBaseFragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return fragments.get(position).getTitle();
    }
}
